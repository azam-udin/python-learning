# Function that returns a value
def add_numbers(a, b):
    result = a + b
    return result

# Use the return value
sum_result = add_numbers(5, 3)
print("Sum:", sum_result)

# Another example
def calculate_area(length, width):
    area = length * width
    return area

room_area = calculate_area(10, 8)
print(f"Room area: {room_area} square feet")